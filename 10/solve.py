def get_xx(lines):
    i, x, xx = 1, 1, {}
    for line in lines:
        xx[i] = x
        line = line.strip()
        if line == 'noop':
            i += 1
        else:
            val = int(line.split()[-1])
            xx[i+1] = x
            x += val
            i += 2


def solve1(lines):
    xx = get_xx(lines)
    checks = (20, 60, 100, 140, 180, 220)
    return sum(check * xx[check] for check in checks)


